def handle_grocery(message):
    msg = message.strip()

    if msg == "3":
        return (
            "*[3] قائمة البقالات:*\n\n"
            "1- بقالة التوفير\n"
            "2- بقالة الراية\n"
            "3- بقالة القرين\n\n"
            "*99 - إطلب*: أرسل طلبك ليُعرض في 'طلباتك'."
        )
    
    elif msg == "99":
        return "🛒 أرسل طلبك الآن للبقالة، وسيتم حفظه في 'طلباتك'."
    
    else:
        return "📌 يرجى اختيار رقم من القائمة أو إرسال 99 لإدخال الطلب."
