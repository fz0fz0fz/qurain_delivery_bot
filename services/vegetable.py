def handle_vegetable(message):
    msg = message.strip()

    if msg == "4":
        return (
            "*[4] قائمة الخضار:*\n\n"
            "1- خضار المدينة\n"
            "2- خضار طازج\n"
            "3- خضار القرين\n\n"
            "*99 - إطلب*: أرسل طلبك ليُعرض في 'طلباتك'."
        )
    
    elif msg == "99":
        return "🥬 أرسل طلبك الآن للخضار، وسيتم حفظه في 'طلباتك'."
    
    else:
        return "📌 يرجى اختيار رقم من القائمة أو إرسال 99 لإدخال الطلب."
